#include <bits/stdc++.h>
using namespace std;

void fastio(bool read = false) 
{
    if(read) 
    {
        #ifndef ONLINE_JUGDE
        freopen("input.txt","r",stdin);
        freopen("output.txt","w",stdout);
        freopen("error.txt","w",stderr);
        #endif  
    }
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    return;
}

#define vss vector<vector<string>>
#define vs vector<string>

class Solution 
{
    vss result;
	bool canPlace(vss &grid, int r, int c, int n) {
		int i = r, j = c;
		while(i >= 0) {
			if(grid[i][j] == "Q")
				return false;
			i--;
		}
		i = r;
		while(i >= 0 and j < n) {
			if(grid[i][j] == "Q")
				return false;
			i--;
			j++;
		}
		i = r, j = c;
		while(i >=0 and j >= 0) {
			if(grid[i][j] == "Q")
				return false;
			i--;
			j--;
		}
		return true;
	}

	bool dfs(vss & grid, int x, int n)
	{
		if(x >= n)
        {
            build(grid, n);
            return false;
        }
		for(int i = 0; i < n; ++i)
		{
			if(canPlace(grid, x, i, n))
			{
				grid[x][i] = "Q";
				bool sucess = dfs(grid, x+1, n);
				if( sucess ) return true;
				grid[x][i] = ".";
			}
		}
		return false;
	}

    void build(vss &grid, int n) {
        vs res;
        for(vs lst: grid) {
            string cur = "";
            for(string s: lst)
                cur.append(s);
            res.push_back(cur);
        }
        result.push_back(res);
    }

public:
	vss solveNQueens(int n) 
	{
    	vss grid(n, vector<string>(n, "."));
    	dfs(grid, 0, n);
    	return result;
    }

    void print(vss &data)
    {
    	cout << "Start\n";
        for(vector<string> list: data)
    	{
    		for(string cur: list)
            	cout << cur << " ";
        	cout << "\n";
        }
        cout << "End\n";
    } 

    void solve()
    {
    	int n;
    	cin >> n;
    	vector<vector<string>> res = solveNQueens(n);
    	print(res);
        return;
    }
};

int main() 
{
    fastio(true);
    int tc;
    cin >> tc;
    for(int t = 1; t <= tc; ++t) 
    {    
        Solution *s = new Solution();
        cout << "TestCase #" << t << " : " << endl;
        s->solve();        
    }
    return 0;
}

